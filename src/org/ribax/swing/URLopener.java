package org.ribax.swing;

/**
 * Interface and class variables for a class that opens a URL.
 *
 * @version <tt>$Revision: $</tt>
 * @author  <a href="mailto:damian@ribax.org">Damian Hamill</a>
 */
public class URLopener {

    public interface Opener {

        public void openURL(String url, String title);
    }
    static public Opener g_Opener = null;

    static public void openURL(String url, String title) {

        if (g_Opener != null) {
            g_Opener.openURL(url, title);
        }
    }
}
